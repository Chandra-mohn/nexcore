fn write_pii_functions(out: &mut String) {
    writeln!(out, "    // =========================================================================").unwrap();
    writeln!(out, "    // PII Encryption/Decryption (mock -- replace with Voltage SDK)").unwrap();
    writeln!(out, "    // =========================================================================").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "    /**").unwrap();
    writeln!(out, "     * Encrypt a value using format-preserving encryption.").unwrap();
    writeln!(out, "     *").unwrap();
    writeln!(out, "     * <p>MOCK IMPLEMENTATION: encodes as ENC[profile]:base64(value).").unwrap();
    writeln!(out, "     * Replace with Voltage SDK for production use.</p>").unwrap();
    writeln!(out, "     */").unwrap();
    writeln!(out, "    public static String encrypt(String value, String profile) {{").unwrap();
    writeln!(out, "        if (value == null) return null;").unwrap();
    writeln!(out, "        String encoded = Base64.getEncoder().encodeToString(value.getBytes(StandardCharsets.UTF_8));").unwrap();
    writeln!(out, "        return MOCK_ENC_PREFIX + profile + MOCK_ENC_SUFFIX + encoded;").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "    /**").unwrap();
    writeln!(out, "     * Decrypt a value encrypted with {{@link #encrypt}}.").unwrap();
    writeln!(out, "     *").unwrap();
    writeln!(out, "     * <p>MOCK IMPLEMENTATION: reverses the mock encoding.").unwrap();
    writeln!(out, "     * Replace with Voltage SDK for production use.</p>").unwrap();
    writeln!(out, "     */").unwrap();
    writeln!(out, "    public static String decrypt(String value, String profile) {{").unwrap();
    writeln!(out, "        if (value == null) return null;").unwrap();
    writeln!(out, "        String prefix = MOCK_ENC_PREFIX + profile + MOCK_ENC_SUFFIX;").unwrap();
    writeln!(out, "        if (!value.startsWith(prefix)) return value;").unwrap();
    writeln!(out, "        String encoded = value.substring(prefix.length());").unwrap();
    writeln!(out, "        return new String(Base64.getDecoder().decode(encoded), StandardCharsets.UTF_8);").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "    /**").unwrap();
    writeln!(out, "     * Mask a value using a pattern. '#' = show, '*' = mask, other = literal.").unwrap();
    writeln!(out, "     */").unwrap();
    writeln!(out, "    public static String mask(String value, String pattern) {{").unwrap();
    writeln!(out, "        if (value == null || pattern == null) return value;").unwrap();
    writeln!(out, "        StringBuilder sb = new StringBuilder();").unwrap();
    writeln!(out, "        int vi = value.length() - 1;").unwrap();
    writeln!(out, "        for (int pi = pattern.length() - 1; pi >= 0; pi--) {{").unwrap();
    writeln!(out, "            char pc = pattern.charAt(pi);").unwrap();
    writeln!(out, "            if (pc == '#' && vi >= 0) {{ sb.insert(0, value.charAt(vi--)); }}").unwrap();
    writeln!(out, "            else if (pc == '*' && vi >= 0) {{ sb.insert(0, '*'); vi--; }}").unwrap();
    writeln!(out, "            else {{ sb.insert(0, pc); }}").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "        return sb.toString();").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "    /** SHA-256 hash (irreversible). */").unwrap();
    writeln!(out, "    public static String hash(String value) {{").unwrap();
    writeln!(out, "        if (value == null) return null;").unwrap();
    writeln!(out, "        try {{").unwrap();
    writeln!(out, "            MessageDigest md = MessageDigest.getInstance(\"SHA-256\");").unwrap();
    writeln!(out, "            byte[] digest = md.digest(value.getBytes(StandardCharsets.UTF_8));").unwrap();
    writeln!(out, "            return HexFormat.of().formatHex(digest);").unwrap();
    writeln!(out, "        }} catch (NoSuchAlgorithmException e) {{").unwrap();
    writeln!(out, "            throw new RuntimeException(\"SHA-256 not available\", e);").unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "    /** SHA-256 hash with salt. */").unwrap();
    writeln!(out, "    public static String hash(String value, String salt) {{").unwrap();
    writeln!(out, "        if (value == null) return null;").unwrap();
    writeln!(out, "        return hash(salt + value);").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();
}

